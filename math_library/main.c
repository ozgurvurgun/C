#include <math.h>

int main()
{
    //absolute
    printf("%d\n", abs(-5));
    int x = pow(2,5);

    //pow
    printf("%d\n", x);

    //floor
    printf("%f\n", floor(12.87)); //en yakin asagi tam sayi

    //ceil
    printf("%f\n", ceil(12.87));  //en yakin yukari yuvarlar

    //square root
    printf("%f\n", sqrt(81));

    //E^2
    printf("%f\n", exp(2));

    return 0;
}
